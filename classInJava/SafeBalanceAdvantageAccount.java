package core_java_topics.classInJava;

public class SafeBalanceAdvantageAccount {

	// instance variable
	private long sa_acc_num;
	private String sa_acc_holder;
	private long ssn_num;
	private String city;
	private String ifsc_code;
	private double sa_acc_balance;
	
	public long getSa_acc_num() {
		return sa_acc_num;
	}
	public void setSa_acc_num(long sa_acc_num) {
		this.sa_acc_num = sa_acc_num;
	}
	public String getSa_acc_holder() {
		return sa_acc_holder;
	}
	public void setSa_acc_holder(String sa_acc_holder) {
		this.sa_acc_holder = sa_acc_holder;
	}
	public long getSsn_num() {
		return ssn_num;
	}
	public void setSsn_num(long ssn_num) {
		this.ssn_num = ssn_num;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getIfsc_code() {
		return ifsc_code;
	}
	public void setIfsc_code(String ifsc_code) {
		this.ifsc_code = ifsc_code;
	}
	public double getSa_acc_balance() {
		return sa_acc_balance;
	}
	public void setSa_acc_balance(double sa_acc_balance) {
		this.sa_acc_balance = sa_acc_balance;
	}
	
	
}
