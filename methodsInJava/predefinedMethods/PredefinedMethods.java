package core_java_topics.methodsInJava.predefinedMethods;

public class PredefinedMethods {

	public static void main(String[] args) {
		String name = "kamran";
		System.out.println("Length of Name is : " + name.length());
		System.out.println("Maximum number is : " + Math.max(65, 32));
		System.out.println("Minimum number is : " + Math.min(75, 18));
	}

}
